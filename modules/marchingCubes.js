// Interpolate between two grid points where the isosurface crosses
function interpolate(p1, p2, v1, v2, isolevel) {
  if (Math.abs(v1 - isolevel) < 1e-10) return p1.slice();
  if (Math.abs(v2 - isolevel) < 1e-10) return p2.slice();
  if (Math.abs(v1 - v2) < 1e-10) return p1.slice();
  const t = (isolevel - v1) / (v2 - v1);
  return [
    p1[0] + t * (p2[0] - p1[0]),
    p1[1] + t * (p2[1] - p1[1]),
    p1[2] + t * (p2[2] - p1[2]),
  ];
}

// Extract an isosurface mesh from a 3D scalar field
// Returns { positions: Float32Array, normals: Float32Array }
export function marchingCubes(values, gridSize, bounds, isolevel) {
  const positions = [];
  const step = (bounds.max - bounds.min) / (gridSize - 1);

  function idx(x, y, z) {
    return z * gridSize * gridSize + y * gridSize + x;
  }

  function pos(x, y, z) {
    return [
      bounds.min + x * step,
      bounds.min + y * step,
      bounds.min + z * step,
    ];
  }

  for (let z = 0; z < gridSize - 1; z++) {
    for (let y = 0; y < gridSize - 1; y++) {
      for (let x = 0; x < gridSize - 1; x++) {
        // 8 corner values
        const v = [
          values[idx(x,   y,   z)],
          values[idx(x+1, y,   z)],
          values[idx(x+1, y+1, z)],
          values[idx(x,   y+1, z)],
          values[idx(x,   y,   z+1)],
          values[idx(x+1, y,   z+1)],
          values[idx(x+1, y+1, z+1)],
          values[idx(x,   y+1, z+1)],
        ];

        // 8 corner positions
        const p = [
          pos(x,   y,   z),
          pos(x+1, y,   z),
          pos(x+1, y+1, z),
          pos(x,   y+1, z),
          pos(x,   y,   z+1),
          pos(x+1, y,   z+1),
          pos(x+1, y+1, z+1),
          pos(x,   y+1, z+1),
        ];

        // Determine cube index
        let cubeIndex = 0;
        for (let i = 0; i < 8; i++) {
          if (v[i] >= isolevel) cubeIndex |= (1 << i);
        }

        const edges = EDGE_TABLE[cubeIndex];
        if (edges === 0) continue;

        // Compute edge vertices
        const edgeVerts = new Array(12);
        const edgePairs = [
          [0,1],[1,2],[2,3],[3,0],
          [4,5],[5,6],[6,7],[7,4],
          [0,4],[1,5],[2,6],[3,7],
        ];

        for (let i = 0; i < 12; i++) {
          if (edges & (1 << i)) {
            const [a, b] = edgePairs[i];
            edgeVerts[i] = interpolate(p[a], p[b], v[a], v[b], isolevel);
          }
        }

        // Generate triangles
        const tris = TRI_TABLE[cubeIndex];
        for (let i = 0; i < tris.length; i += 3) {
          positions.push(
            ...edgeVerts[tris[i]],
            ...edgeVerts[tris[i+1]],
            ...edgeVerts[tris[i+2]],
          );
        }
      }
    }
  }

  // Compute normals from triangle face normals
  const posArray = new Float32Array(positions);
  const normals = new Float32Array(posArray.length);

  for (let i = 0; i < posArray.length; i += 9) {
    const ax = posArray[i+3] - posArray[i];
    const ay = posArray[i+4] - posArray[i+1];
    const az = posArray[i+5] - posArray[i+2];
    const bx = posArray[i+6] - posArray[i];
    const by = posArray[i+7] - posArray[i+1];
    const bz = posArray[i+8] - posArray[i+2];
    let nx = ay * bz - az * by;
    let ny = az * bx - ax * bz;
    let nz = ax * by - ay * bx;
    const len = Math.sqrt(nx*nx + ny*ny + nz*nz) || 1;
    nx /= len; ny /= len; nz /= len;
    for (let j = 0; j < 3; j++) {
      normals[i + j*3]     = nx;
      normals[i + j*3 + 1] = ny;
      normals[i + j*3 + 2] = nz;
    }
  }

  return { positions: posArray, normals };
}
